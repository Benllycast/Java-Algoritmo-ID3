    private void startGUI() {

        jLabel3.setVisible(false);
        jProgressBar1.setVisible(false);

        jButton1.addActionListener( new ActionListener() {
            public void actionPerformed(ActionEvent e) {

                ResultSet rs = manager.getResultSet( sql.getText() );

                if ( rs != null ) {
                    dataset = rs;
                    cargarDataSet(rs);
                }
            }
        } );

        jButton3.addActionListener( new ActionListener() {
            public void actionPerformed(ActionEvent e) {

                informar( "Generando arbol de desicion, por favor espere." );

                try {

                    int selecc = jList1.getSelectedIndex();

                    if ( selecc < 0 ) {
                        JOptionPane.showMessageDialog(null, "Por favor Seloeccione una clase.");
                        return;
                    }

                    String clase = jList1.getSelectedValue().toString();
                    ID3 minero = new ID3(dataset, clase);
                    minero.createDecisionTree();
                    
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        } );

        jList1.addListSelectionListener( new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent e) {
                jLabel4.setText( jList1.getSelectedValue().toString() );
            }
        } );

        jButton3.addActionListener(new ActionListener() {

            public void actionPerformed(ActionEvent e) {
               new VistaConsulta().setVisible(true);
            }
        });

    }
