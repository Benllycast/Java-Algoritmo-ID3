    private void cargarDataSet(ResultSet rs) {
        try {

            ResultSetMetaData meta = rs.getMetaData();
            int atrib = meta.getColumnCount();

            String atributos[] = new String[atrib];

            for (int i = 0; i < atributos.length; i++) {
                atributos[i] = meta.getColumnLabel(i+1);
            }

            DefaultTableModel dtm = new DefaultTableModel(atributos, 0);

            while ( rs.next() ) {
                
                String data[] = new String[atrib];

                for (int i = 0; i < data.length; i++) {
                    data[i] = rs.getObject(i+1).toString();
                }

                dtm.addRow(data);
            }

            jTable1.setModel(dtm);

            DefaultListModel dlm = new DefaultListModel();

            for (int i = 0; i < atributos.length; i++) {
                dlm.addElement( atributos[i] );
            }

            jList1.setModel(dlm);

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

